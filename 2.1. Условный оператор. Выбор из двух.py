# 8. Напишите программу, которая по введённому возрасту пользователя сообщает, к какой возрастной группе он относится:
# до 13 включительно – детство;
# от 14 до 24 – молодость;
# от 25 до 59 – зрелость;
# от 60 – старость.
# На вход программе подаётся одно целое число – возраст пользователя.
# Программа должна вывести название возрастной группы.

age = int(input())
if 0 < age <= 13:
    print("детство")
if 13 < age <= 24:
    print("молодость")
if 25 <= age <= 59:
    print("зрелость")
if 60 <= age:
    print("старость")

# 9. Напишите программу, которая считывает три числа и подсчитывает сумму только положительных чисел.
# На вход программе подаются три целых числа.
# Программа должна вывести одно число – сумму положительных чисел.
# Примечание. Если положительных чисел нет, то следует вывести 0

a, b, c = int(input()), int(input()), int(input())
total = 0
if a > 0:
    total = total + a
if b > 0:
    total = total + b
if c > 0:
    total = total + c
print(total)
