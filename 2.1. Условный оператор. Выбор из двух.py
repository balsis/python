# 3. Напишите программу, которая проверяет, что для заданного четырехзначного числа выполняется следующее соотношение: сумма первой и последней цифр равна разности второй и третьей цифр.

a = input()
first = a[0]
second = a[1]
third = a[2]
last = a[3]
if int(first) + int(last) == int(second) - int(third):
    print("ДА")
else:
    print("НЕТ")

